// 用于存储已使用的图标，确保不重复
const usedIcons = new Set<string>()

// 在 getRandomIcon 函数之前添加一个新的函数，用于获取像素风格图标
export function getRandomPixelIcon(index: number, totalCount = 10) {
  return PixelIcons.getRandomPixelIcon(index, totalCount)
}

/**
 * 根据文本内容匹配最合适的图标
 * @param text 需要匹配图标的文本
 * @param messageId 消息ID，用于重置已使用图标集
 * @param index 当前项的索引
 * @param totalItems 总项数
 * @returns 匹配的图标组件
 */
export function matchIconToText(text: string, messageId: string, index: number, totalItems: number): React.ReactNode {
  // 如果是新消息或第一个项目，重置已使用图标集
  if (index === 0) {
    usedIcons.clear()
  }

  // 使用像素风格图标
  return getRandomPixelIcon(index, totalItems)
}
